#include <iostream>

using namespace std;
int main()
{
	setlocale(LC_ALL, "rus");
	int a, b, S, R, P;
	float D;
	cout << "Введите значение пары чисел, не равных нулю:";
	cin >> a >> b;
	S = a + b;
	R = a - b;
	P = a * b;
	D = sqrt(a) / sqrt(b);
	cout << "Сумма равна: " << S << "\n"
		<< "Разница равна: " << R << "\n"
		<< "Произвдение равно: " << P << "\n"
		<< "Часное квадратов равно: " << D;
}
